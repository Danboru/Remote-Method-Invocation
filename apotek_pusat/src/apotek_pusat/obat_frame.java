    private static java.util.List<services.ModelObat> selectObat() {
        services.ApotekService_Service service = new services.ApotekService_Service();
        services.ApotekService port = service.getApotekServicePort();
        return port.selectObat();
    }

    private static java.util.List<services.ModelObat> insertObat(java.lang.String kodeObat, java.lang.String namaObat, java.lang.String golongan, int hargaSatuan, java.lang.String kadaluarsa) {
        services.ApotekService_Service service = new services.ApotekService_Service();
        services.ApotekService port = service.getApotekServicePort();
        return port.insertObat(kodeObat, namaObat, golongan, hargaSatuan, kadaluarsa);
    }

    private static java.util.List<services.ModelObat> deleteObat(int id) {
        services.ApotekService_Service service = new services.ApotekService_Service();
        services.ApotekService port = service.getApotekServicePort();
        return port.deleteObat(id);
    }

    private static java.util.List<services.ModelObat> updateObat(int id, java.lang.String kodeObat, java.lang.String namaObat, java.lang.String golongan, int hargaSatuan, java.lang.String kadaluarsa) {
        services.ApotekService_Service service = new services.ApotekService_Service();
        services.ApotekService port = service.getApotekServicePort();
        return port.updateObat(id, kodeObat, namaObat, golongan, hargaSatuan, kadaluarsa);
    }
