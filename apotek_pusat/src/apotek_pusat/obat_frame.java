    public void isiTeks() {
        services.ModelObat mo = selectObat().get(tbl_obt.getSelectedRow());
        txt_kodeObat.setText(mo.getKodeObat());
        txt_namaObat.setText(mo.getNamaObat());
        txt_harga.setText(String.valueOf(mo.getHargaSatuan()));
        txt_kadaluarsa.setText(mo.getKadaluarsa());
        combo_golongan.setSelectedItem(mo.getGolongan());

        txt_id.setText(String.valueOf(mo.getIdObat()));

        btn_simpan.setEnabled(false);
        btn_save.setEnabled(true);
        btn_hapus.setEnabled(true);
    }

    public void lihat_obat() {
        if (!selectObat().isEmpty()) {
            Object[][] data = new Object[selectObat().size()][6];
            int i = 0;
            int nomor = 1;
            for (services.ModelObat d : selectObat()) {
                data[i][0] = nomor;
                data[i][1] = d.getKodeObat();
                data[i][2] = d.getNamaObat();
                data[i][3] = d.getGolongan();
                data[i][4] = d.getHargaSatuan();
                data[i][5] = d.getKadaluarsa();
                ++i;
                ++nomor;
            }
            this.tbl_obt.setModel(new DefaultTableModel(data, new String[]{"Nomor", "Kode Obat", "Nama Obat", "Golongan", "Harga Satuan", "Kadaluarsa"}));
            jScrollPane1.setViewportView(tbl_obt);
        } else {
            tbl_obt.setEnabled(true);
        }
    }

